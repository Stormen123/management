package com.management.b1.dto;

public class AmountDTO {

	private String amt_code;
	private String amt_grade;
	private String amt_item;
	private String amt_day;
	private String amt_month;
	private String amt_amount;
	
	public String getAmt_code() {
		return amt_code;
	}
	public void setAmt_code(String amt_code) {
		this.amt_code = amt_code;
	}
	public String getAmt_grade() {
		return amt_grade;
	}
	public void setAmt_grade(String amt_grade) {
		this.amt_grade = amt_grade;
	}
	public String getAmt_item() {
		return amt_item;
	}
	public void setAmt_item(String amt_item) {
		this.amt_item = amt_item;
	}
	public String getAmt_day() {
		return amt_day;
	}
	public void setAmt_day(String amt_day) {
		this.amt_day = amt_day;
	}
	public String getAmt_month() {
		return amt_month;
	}
	public void setAmt_month(String amt_month) {
		this.amt_month = amt_month;
	}
	public String getAmt_amount() {
		return amt_amount;
	}
	public void setAmt_amount(String amt_amount) {
		this.amt_amount = amt_amount;
	}
	
}
